    public class EmailService : IIdentityMessageService
    {
        private IdentityMessage receivedIdentityMessage;
        private SmtpClient client;
        private string senderEmailName = "distribuidorasaturno@gmail.com";
        private string senderEmailPassword = "enhonoraestructurasdedatosychivitos";
        private MailMessage message;

        public Task SendAsync(IdentityMessage myReceivedMessage)
        {
            //ver http://stackoverflow.com/questions/31523276/usermanager-sendemailasync-no-email-sent donde yo mismo publiqué esta solución.
            receivedIdentityMessage = myReceivedMessage;
            configuringSMTPClient();
            configuringMessage();
            return client.SendMailAsync(message);
        }

        private void configuringSMTPClient()
        {
            client = new SmtpClient();
            client.Port = 587;
            client.Host = "smtp.gmail.com";
            client.EnableSsl = true;
            client.Timeout = 10000;
            client.DeliveryMethod = SmtpDeliveryMethod.Network;
            client.UseDefaultCredentials = false;
            client.Credentials = new NetworkCredential(senderEmailName, senderEmailPassword);
        }

        private void configuringMessage()
        {
            string destination = receivedIdentityMessage.Destination;
            string subject = receivedIdentityMessage.Subject;
            string body = receivedIdentityMessage.Body;
            message = new MailMessage(senderEmailName, destination, subject, body);
            message.BodyEncoding = UTF8Encoding.UTF8;
            message.DeliveryNotificationOptions = DeliveryNotificationOptions.OnFailure;
            message.IsBodyHtml = true;
        }
    }

